

// Loop externo para os multiplicadores de 1 a 10
for (let i = 1; i <= 10; i++) {

    console.log(`Tabuada de ${i}:`)

    // Loop interno para os multiplicandos de 1 a 10
    for (let j = 1; j <= 10; j++) {
        console.log(`${i} x ${j} = ${i * j}`)
    }
 
    console.log("                                 ")


}

